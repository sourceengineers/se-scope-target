void printHandle(IByteStreamHandle stream){

	while(stream->byteIsReady(stream) == true){
		uint8_t data = stream->readByte(stream);

		HAL_UART_Transmit(&huart2, &data, 1, 500);
	}
	HAL_UART_Transmit(&huart2, (uint8_t*) '\n', 1, 500);
	HAL_UART_Transmit(&huart2, (uint8_t*) '\n', 1, 500);
	HAL_UART_Transmit(&huart2, (uint8_t*) '\n', 1, 500);
	HAL_UART_Transmit(&huart2, (uint8_t*) '\n', 1, 500);
	HAL_UART_Transmit(&huart2, (uint8_t*) '\n', 1, 500);

}

void receive(ScopeHandle scope){

	uint8_t data[200];
	memset(data, 0, 200);

	HAL_UART_Receive(&huart2, data, 200, 1000);

	if(data[0] != 0){
		//HAL_UART_Transmit(&huart2, data, 200, 5000);

		IByteStreamHandle stream  = Scope_getInputStream(scope);
		stream->write(stream, data, 200);
		Scope_receiveData(scope);
	}
}

